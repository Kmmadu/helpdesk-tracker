@router.get("/me", response_model=UserSchema)
def get_current_user_info(current_user: User = Depends(get_current_user)):
    return current_user

@router.post("/logout")
def logout():
    # JWT is stateless, client just discards the token
    return {"message": "Logged out successfully"}